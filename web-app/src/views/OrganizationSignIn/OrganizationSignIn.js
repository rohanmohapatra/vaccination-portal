const OrganizationSignIn = props => {
  const { history } = props;

  const classes = useStyles();

  const [formState, setFormState] = useState({
    isValid: false,
    values: {},
    touched: {},
    errors: {}
  });
  const [open, setOpen] = React.useState(false);

  const handleClick = () => {
    setOpen(true);
  };

  const handleClose = (event, reason) => {
    if (reason === 'clickaway') {
      return;
    }

    setOpen(false);
  };

  useEffect(() => {
    const errors = validate(formState.values, schema);

    setFormState(formState => ({
      ...formState,
      isValid: errors ? false : true,
      errors: errors || {}
    }));
  }, [formState.values]);

  const handleBack = () => {
    history.goBack();
  };

  const handleChange = event => {
    event.persist();

    setFormState(formState => ({
      ...formState,
      values: {
        ...formState.values,
        [event.target.name]:
          event.target.type === 'checkbox'
            ? event.target.checked
            : event.target.value
      },
      touched: {
        ...formState.touched,
        [event.target.name]: true
      }
    }));
  };

  const handleOrganizationSignIn = event => {
    event.preventDefault();
    //history.push('/');
    axios.post("https://cavach-api.herokuapp.com/api/organization/organization_login/", formState.values)
    .then(function(response){
      console.log(response);
      localStorage.setItem("organization_id",response.data.organization_id);
      history.push("/organization/details");
    })
    .catch(function (response) {
      //handle error
      console.log(response);
      
      setOpen(true);
    });
  };

  const hasError = field =>
    formState.touched[field] && formState.errors[field] ? true : false;

  return (
    <div className={classes.root}>
      <Grid
        className={classes.grid}
        container
      >
        <Grid
          className={classes.quoteContainer}
          item
          lg={5}
        >
          <div className={classes.quote}>
            <div className={classes.quoteInner}>
              <Typography
                className={classes.quoteText}
                variant="h1"
              >
                Keep your Vaccination Records Safe
              </Typography>
              <br />
              <Typography
                className={classes.quoteText}
                variant="h1"
              >
                Certified by Organizations
              </Typography>
              <br />
              <Typography
                className={classes.quoteText}
                variant="h1"
              >
                CAVACH : Context aided vaccination automation for Child Health
              </Typography>
              <div className={classes.person}>
                <Typography
                  className={classes.name}
                  variant="h4"
                >
                  Made with <FavoriteIcon className={classes.love}/> by Rohan, Sanat and Shivani
                </Typography>
              </div>
            </div>
          </div>
        </Grid>
        <Grid
          className={classes.content}
          item
          lg={7}
          xs={12}
        >
          <div className={classes.content}>
            <div className={classes.contentHeader}>
              <IconButton onClick={handleBack}>
                <ArrowBackIcon />
              </IconButton>
            </div>
            <div className={classes.contentBody}>
              <form
                className={classes.form}
                onSubmit={handleOrganizationSignIn}
              >
                <Typography
                  className={classes.title}
                  variant="h2"
                >
                  Sign in
                </Typography>
                <Typography
                  align="center"
                  className={classes.sugestion}
                  color="textSecondary"
                  variant="body1"
                >
                  Login with username address
                </Typography>
                <TextField
                  className={classes.textField}
                  error={hasError('username')}
                  fullWidth
                  helperText={
                    hasError('username') ? formState.errors.username[0] : null
                  }
                  label="UserName"
                  name="username"
                  onChange={handleChange}
                  type="text"
                  value={formState.values.username || ''}
                  variant="outlined"
                />
                <TextField
                  className={classes.textField}
                  error={hasError('password')}
                  fullWidth
                  helperText={
                    hasError('password') ? formState.errors.password[0] : null
                  }
                  label="Password"
                  name="password"
                  onChange={handleChange}
                  type="password"
                  value={formState.values.password || ''}
                  variant="outlined"
                />
                <Button
                  className={classes.OrganizationSignInButton}
                  color="primary"
                  disabled={!formState.isValid}
                  fullWidth
                  size="large"
                  type="submit"
                  variant="contained"
                >
                  Sign in now
                </Button>
              </form>
              <Snackbar
                anchorOrigin={{vertical: 'bottom',horizontal: 'right'}}
                open={open}
                autoHideDuration={6000}
                onClose={handleClose}
              >
                <MySnackbarContentWrapper
                  variant="error"
                  className={classes.margin}
                  message="Username or Password might be wrong!"
                />
              </Snackbar>
            </div>
          </div>
        </Grid>
      </Grid>
    </div>
  );
};

OrganizationSignIn.propTypes = {
  history: PropTypes.object
};

export default withRouter(OrganizationSignIn);
